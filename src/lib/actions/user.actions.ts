"use server";

import { revalidatePath } from "next/cache";
import { auth } from "@/auth";
import prisma from "@/lib/prisma";

export async function updateUserProfileImage(imageUrl: string) {
  console.log(`üñºÔ∏è [ACTION] updateUserProfileImage`);
  const session = await auth();
  const userId = session?.user?.id;

  if (!userId) {
    console.error("‚ùå Erreur: Tentative de mise √† jour d'image sans session.");
    throw new Error("Non autoris√©");
  }

  console.log(`  -> pour l'utilisateur ${userId}.`);

  try {
    await prisma.user.update({
      where: { id: userId },
      data: { image: imageUrl },
    });

    console.log("   -> Image de profil mise √† jour en base de donn√©es.");

    // Revalide toutes les pages qui pourraient afficher l'avatar de l'utilisateur.
    // 'layout' est une option puissante pour revalider tout le site.
    revalidatePath('/', 'layout');
    
    console.log(`‚úÖ [ACTION] Mise √† jour de l'image de profil r√©ussie pour ${userId}.`);
  } catch (error) {
    console.error("‚ùå Erreur lors de la mise √† jour de l'image de profil:", error);
    throw new Error("Impossible de mettre √† jour l'image de profil.");
  }
}
