export async function updateUserProfileImage(imageUrl: string) {
  console.log(`üñºÔ∏è [ACTION] updateUserProfileImage`);
  const session = await auth();
  const userId = session?.user?.id;

  if (!userId) {
    console.error("‚ùå Erreur: Tentative de mise √† jour d'image sans session.");
    throw new Error("Non autoris√©");
  }

  console.log(`  -> pour l'utilisateur ${userId}.`);

  try {
    await prisma.user.update({
      where: { id: userId },
      data: { image: imageUrl },
    });

    console.log("   -> Image de profil mise √† jour en base de donn√©es.");

    revalidatePath('/', 'layout');
    
    console.log(`‚úÖ [ACTION] Mise √† jour de l'image de profil r√©ussie pour ${userId}.`);
  } catch (error) {
    console.error("‚ùå Erreur lors de la mise √† jour de l'image de profil:", error);
    throw new Error("Impossible de mettre √† jour l'image de profil.");
  }
}

export async function updateUserSettings(settings: { name?: string; parentPassword?: string }) {
    console.log(`‚öôÔ∏è [ACTION] updateUserSettings`);
    const session = await auth();
    const userId = session?.user?.id;
  
    if (!userId) {
      console.error("‚ùå Erreur: Tentative de mise √† jour des param√®tres sans session.");
      throw new Error("Non autoris√©");
    }

    const dataToUpdate: { name?: string; parentPassword?: string | null } = {};

    if (settings.name) {
        if (settings.name.length < 2) throw new Error("Le nom est trop court.");
        dataToUpdate.name = settings.name;
        console.log(`  -> Mise √† jour du nom pour ${userId}.`);
    }

    if (settings.parentPassword !== undefined && session.user.role === 'ELEVE') {
        if (settings.parentPassword && settings.parentPassword.length < 4) {
            throw new Error("Le mot de passe parental est trop court.");
        }
        // CORRECTION : Assigner `null` si la cha√Æne est vide, sinon la cha√Æne elle-m√™me.
        dataToUpdate.parentPassword = settings.parentPassword || null;
        console.log(`  -> Mise √† jour du mot de passe parental pour l'√©l√®ve ${userId}.`);
    }

    if (Object.keys(dataToUpdate).length === 0) {
        console.log("  -> Aucune donn√©e √† mettre √† jour.");
        return;
    }
  
    try {
      await prisma.user.update({
        where: { id: userId },
        data: dataToUpdate,
      });
  
      console.log(`‚úÖ [ACTION] Param√®tres mis √† jour pour ${userId}.`);
      revalidatePath('/', 'layout');
      
    } catch (error) {
      console.error(`‚ùå Erreur lors de la mise √† jour des param√®tres pour ${userId}:`, error);
      throw new Error("Impossible de sauvegarder les param√®tres.");
    }
}
