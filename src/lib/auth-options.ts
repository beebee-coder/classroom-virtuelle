// src/lib/auth-options.ts
import { AuthOptions } from "next-auth";
import CredentialsProvider from "next-auth/providers/credentials";
import prisma from "@/lib/prisma";
import type { User as PrismaUser, Role } from '@prisma/client';

// D√©finition de l'interface pour √©tendre le type User
interface CustomUser extends Omit<PrismaUser, 'emailVerified'> {
  emailVerified: Date | null;
}

export const authOptions: AuthOptions = {
  providers: [
    CredentialsProvider({
      name: "credentials",
      credentials: {
        email: { label: "Email", type: "email" },
        password: { label: "Password", type: "password" },
      },
      async authorize(credentials) {
        if (!credentials?.email || !credentials?.password) {
          console.error('‚ùå [AUTH - Authorize] √âchec: Email ou mot de passe manquant.');
          return null;
        }

        try {
          console.log(`üîç [AUTH - Authorize] Recherche de l'utilisateur: ${credentials.email}`);
          const user = await prisma.user.findUnique({
            where: { email: credentials.email },
          });

          if (!user) {
            console.warn(`‚ö†Ô∏è [AUTH - Authorize] Utilisateur non trouv√©: ${credentials.email}`);
            return null;
          }

          // Pour la d√©mo, on accepte le mot de passe 'password'
          if (credentials.password !== 'password') {
            console.warn(`üîí [AUTH - Authorize] Mot de passe incorrect pour: ${credentials.email}`);
            return null;
          }

          console.log(`‚úÖ [AUTH - Authorize] Authentification r√©ussie pour: ${user.name} (${user.email})`);
          return user as any;

        } catch (error) {
          console.error("üí• [AUTH - Authorize] Erreur critique:", error);
          return null;
        }
      },
    }),
  ],
  session: {
    strategy: "jwt",
    maxAge: 30 * 24 * 60 * 60, // 30 jours
  },
  pages: {
    signIn: "/login",
    error: "/login",
  },
  callbacks: {
    async jwt({ token, user }) {
      if (user) {
        console.log('üìù [AUTH - JWT Callback] Enrichissement du token lors de la connexion.');
        token.id = user.id;
        const prismaUser = user as CustomUser;
        token.role = prismaUser.role;
        // CORRECTION: Utiliser le bon champ selon le sch√©ma Prisma
        // Le sch√©ma montre que User a 'classeId' (relation avec Classroom)
        token.classeId = prismaUser.classeId;
      }
      
      // V√©rifier p√©riodiquement la validit√© de l'utilisateur
      if (token.id) {
        try {
          const dbUser = await prisma.user.findUnique({
            where: { id: token.id as string },
            select: { id: true, role: true, classeId: true }
          });
          
          if (dbUser) {
            token.role = dbUser.role;
            token.classeId = dbUser.classeId;
          }
        } catch (error) {
          console.error('‚ùå [AUTH - JWT Callback] Erreur de v√©rification utilisateur:', error);
        }
      }
      
      return token;
    },
    async session({ session, token }) {
      if (token && session.user) {
        session.user.id = token.id as string;
        session.user.role = token.role as Role;
        // CORRECTION: Utiliser le bon champ selon le sch√©ma Prisma
        session.user.classeId = token.classeId as string | undefined;
        
        console.log(`üîê [AUTH - Session Callback] Session cr√©√©e pour: ${session.user.name} (ID: ${session.user.id}, R√¥le: ${session.user.role})`);
      }
      return session;
    },
    async redirect({ url, baseUrl }) {
      // Rediriger vers la page d'origine ou le dashboard par d√©faut
      if (url.startsWith(baseUrl)) return url;
      return baseUrl;
    },
  },
  events: {
    async signIn({ user, isNewUser }) {
      console.log(`üîî [AUTH - Event] Connexion de ${user.email} - Nouvel utilisateur: ${isNewUser}`);
    },
    async signOut({ token }) {
      console.log(`üîî [AUTH - Event] D√©connexion de l'utilisateur ID: ${token?.id}`);
    },
    async session({ session }) {
      console.log(`üîî [AUTH - Event] Session utilis√©e par: ${session.user?.email}`);
    },
  },
  debug: process.env.NODE_ENV === 'development',
};