// src/lib/auth-options.ts
import { AuthOptions } from "next-auth";
import CredentialsProvider from "next-auth/providers/credentials";
import prisma from "@/lib/prisma";
import type { User as PrismaUser, Role } from '@prisma/client';

// D√©finition de l'interface pour √©tendre le type User
interface CustomUser extends Omit<PrismaUser, 'emailVerified'> {
  emailVerified: Date | null;
}

export const authOptions: AuthOptions = {
  providers: [
    CredentialsProvider({
      name: "credentials",
      credentials: {
        email: { label: "Email", type: "email" },
        password: { label: "Password", type: "password" },
      },
      async authorize(credentials) {
        if (!credentials?.email || !credentials?.password) {
          console.error('‚ùå [AUTH - Authorize] √âchec: Email ou mot de passe manquant.');
          return null;
        }

        try {
          console.log(`üîç [AUTH - Authorize] Recherche de l'utilisateur: ${credentials.email}`);
          const user = await prisma.user.findUnique({
            where: { email: credentials.email },
          });

          if (!user) {
            console.warn(`‚ö†Ô∏è [AUTH - Authorize] Utilisateur non trouv√©: ${credentials.email}`);
            return null;
          }

          // Pour la d√©mo, on accepte le mot de passe 'password'
          if (credentials.password !== 'password') {
            console.warn(`üîí [AUTH - Authorize] Mot de passe incorrect pour: ${credentials.email}`);
            return null;
          }

          console.log(`‚úÖ [AUTH - Authorize] Authentification r√©ussie pour: ${user.name} (${user.email})`);
          return user as any;

        } catch (error) {
          console.error("üí• [AUTH - Authorize] Erreur critique:", error);
          return null;
        }
      },
    }),
  ],
  session: {
    strategy: "jwt",
    maxAge: 30 * 24 * 60 * 60, // 30 jours
  },
  pages: {
    signIn: "/login",
    error: "/login",
  },
  callbacks: {
    async jwt({ token, user }) {
      if (user) {
        console.log('üìù [AUTH - JWT Callback] Enrichissement du token lors de la connexion.');
        token.id = user.id;
        const prismaUser = user as CustomUser;
        token.role = prismaUser.role;
        token.classeId = prismaUser.classeId;
      }
      return token;
    },
    async session({ session, token }) {
      if (token && session.user) {
        session.user.id = token.id as string;
        session.user.role = token.role as Role;
        session.user.classeId = token.classeId as string | undefined;
      }
      return session;
    },
  },
};
