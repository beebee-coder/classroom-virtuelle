import Redis from 'ioredis';

let redisClient: Redis | null = null;

// Fonction pour obtenir le client Redis
export async function getClient(): Promise<Redis | null> {
  // Si le client est d√©j√† initialis√©, le retourner
  if (redisClient) {
    return redisClient;
  }

  // Si REDIS_URL n'est pas d√©finie, retourner null
  if (!process.env.REDIS_URL) {
    console.warn('‚ö†Ô∏è REDIS_URL non d√©finie, Redis d√©sactiv√©');
    return null;
  }

  try {
    // Initialiser le client Redis
    redisClient = new Redis(process.env.REDIS_URL, {
      retryStrategy: (times: number) => {
        if (times > 3) return null;
        const delay = Math.min(times * 500, 2000);
        console.log(`üîå Redis reconnexion n¬∞${times} dans ${delay}ms`);
        return delay;
      },
      maxRetriesPerRequest: 1,
    });

    // Gestion des √©v√©nements
    redisClient.on('error', (err: Error) => {
      console.error('‚ùå Erreur Redis:', err.message);
    });

    redisClient.on('connect', () => {
      console.log('‚úÖ Connect√© √† Redis avec succ√®s');
    });

    redisClient.on('ready', () => {
      console.log('‚úÖ Redis pr√™t √† recevoir des commandes');
    });

    return redisClient;
  } catch (error) {
    console.error('‚ùå √âchec de l initialisation Redis:', error);
    redisClient = null;
    return null;
  }
}

// Export par d√©faut pour la compatibilit√©
export default getClient;