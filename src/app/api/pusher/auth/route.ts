import { NextResponse } from 'next/server';
import { getServerSession } from 'next-auth';
import { authenticateUser } from '@/lib/pusher/server';
import { authOptions } from '@/lib/auth-options';

export async function POST(request: Request) {
  console.log('üö™ [API PUSHER AUTH] - Requ√™te d\'authentification re√ßue.');
  try {
    // üí° CORRECTION : Le client Pusher envoie les donn√©es en JSON, pas en formData.
    const body = await request.json();
    const { socket_id: socketId, channel_name: channel } = body;
    console.log(`  Socket ID: ${socketId}, Channel: ${channel}`);

    if (!socketId || !channel) {
        console.error('‚ùå [API PUSHER AUTH] - socket_id ou channel_name manquant dans le corps de la requ√™te.');
        return new NextResponse('Bad Request', { status: 400 });
    }
    
    const session = await getServerSession(authOptions);

    if (!session || !session.user) {
        console.error('‚ùå [API PUSHER AUTH] - √âchec : Aucune session utilisateur trouv√©e.');
        return new NextResponse('Unauthorized', { status: 403 });
    }

    const { user } = session;
    console.log(`‚úÖ [API PUSHER AUTH] - Session trouv√©e pour: ${user.name} (ID: ${user.id})`);

    const userData = {
      user_id: user.id,
      user_info: {
        name: user.name,
        role: user.role,
        image: user.image,
      },
    };
    
    console.log('  Pr√©paration des donn√©es utilisateur pour Pusher:', userData);
    const authResponse = await authenticateUser(socketId, channel, userData);
    console.log('‚úÖ [API PUSHER AUTH] - Autorisation r√©ussie. R√©ponse envoy√©e au client.');
    
    return NextResponse.json(authResponse);

  } catch (error) {
    console.error('üí• [API PUSHER AUTH] - Erreur interne du serveur:', error);
    return new NextResponse('Internal Server Error', { status: 500 });
  }
}
