import { authenticateUser } from '@/lib/pusher/server';
import { getAuthSession } from '@/lib/session';
import { NextResponse } from 'next/server';

export async function POST(request: Request) {
  console.log('--- üîê [PUSHER AUTH] - D√©but du processus d\'authentification ---');
  try {
    const session = await getAuthSession();
    console.log('üîê [PUSHER AUTH] - Session r√©cup√©r√©e:', session ? { user: session.user } : 'null');
    
    if (!session?.user?.id) {
      console.error('‚ùå [PUSHER AUTH] - Non autoris√©: session ou ID utilisateur manquant.');
      return new NextResponse('Unauthorized', { status: 401 });
    }

    const body = await request.formData();
    const socketId = body.get('socket_id') as string;
    const channel = body.get('channel_name') as string;
    console.log('üîê [PUSHER AUTH] - Infos re√ßues:', { socketId, channel });


    const userData = {
      id: session.user.id, // ID critique pour la pr√©sence
      user_info: {
        name: session.user.name,
        role: session.user.role,
      },
    };
    
    console.log('üîê [PUSHER AUTH] - Pr√©paration des donn√©es utilisateur pour Pusher:', userData);
    
    // Pour les canaux de pr√©sence, les donn√©es utilisateur sont requises
    const authResponse = await authenticateUser(socketId, userData);
    
    console.log('‚úÖ [PUSHER AUTH] - Authentification r√©ussie. R√©ponse envoy√©e au client.');
    return NextResponse.json(authResponse);

  } catch (error) {
    console.error('‚ùå [PUSHER AUTH] - Erreur interne du serveur:', error);
    return new NextResponse('Internal Server Error', { status: 500 });
  }
}
