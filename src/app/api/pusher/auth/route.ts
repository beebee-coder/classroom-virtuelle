
import { authenticateUser } from "@/lib/pusher/server";
import { getAuthSession } from "@/lib/session";
import { Role } from "@/lib/types";
import { NextResponse } from "next/server";

export async function POST(request: Request) {
  console.log('--- üîê [PUSHER AUTH] - D√©but du processus d\'authentification ---');
  try {
    
    const session = await getAuthSession();
    
    // Pour la d√©mo, on utilise des valeurs de secours si la session n'est pas trouv√©e
    const userId = session?.user?.id || `user-id-${Math.random().toString(36).substring(7)}`;
    const userName = session?.user?.name || 'Utilisateur D√©mo';
    const userRole = session?.user?.role || Role.ELEVE;

    console.log(`üôã [PUSHER AUTH] - Session utilisateur (simul√©e/r√©elle): ${userName} (${userId})`);

    const body = await request.formData();
    const socketId = body.get('socket_id') as string;
    const channel = body.get('channel_name') as string;
    console.log(`üì° [PUSHER AUTH] - Infos re√ßues: socketId=${socketId}, channel=${channel}`);

    // Structure correcte pour les canaux de pr√©sence Pusher
    const userData = {
      id: userId,
      user_info: {
        name: userName,
        role: userRole,
      },
    };
    
    console.log('üë§ [PUSHER AUTH] - Pr√©paration des donn√©es utilisateur pour Pusher:', userData);
    
    const authResponse = await authenticateUser(socketId, channel, userData);
    
    console.log('‚úÖ [PUSHER AUTH] - Authentification r√©ussie. R√©ponse envoy√©e au client.');
    return NextResponse.json(authResponse);

  } catch (error) {
    console.error('üí• [PUSHER AUTH] - Erreur interne du serveur:', error);
    return new NextResponse('Internal Server Error', { status: 500 });
  }
}
