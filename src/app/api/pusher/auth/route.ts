import { NextResponse } from 'next/server';
import { getServerSession } from 'next-auth';
import { authenticateUser } from '@/lib/pusher/server';
import { authOptions } from '@/lib/auth-options';

export async function POST(request: Request) {
  console.log('üö™ [API PUSHER AUTH] - Requ√™te d\'authentification re√ßue.');
  
  try {
    // V√©rification que la requ√™te contient du JSON
    const contentType = request.headers.get('content-type');
    if (!contentType || !contentType.includes('application/json')) {
      console.error('‚ùå [API PUSHER AUTH] - Content-Type must be application/json');
      return new NextResponse('Content-Type must be application/json', { status: 400 });
    }

    const body = await request.json();
    const { socket_id: socketId, channel_name: channel } = body;
    console.log(`  Socket ID: ${socketId}, Channel: ${channel}`);

    if (!socketId || !channel) {
      console.error('‚ùå [API PUSHER AUTH] - socket_id ou channel_name manquant');
      return new NextResponse('Bad Request: socket_id and channel_name are required', { status: 400 });
    }
    
    const session = await getServerSession(authOptions);

    if (!session || !session.user) {
      console.error('‚ùå [API PUSHER AUTH] - Aucune session utilisateur trouv√©e');
      return new NextResponse('Unauthorized', { status: 401 });
    }

    const { user } = session;
    console.log(`‚úÖ [API PUSHER AUTH] - Session trouv√©e pour: ${user.name} (ID: ${user.id})`);

    const userData = {
      user_id: user.id!,
      user_info: {
        name: user.name!,
        role: user.role!,
        image: user.image,
      },
    };
    
    console.log('  Pr√©paration des donn√©es utilisateur pour Pusher:', userData);
    const authResponse = await authenticateUser(socketId, channel, userData);
    console.log('‚úÖ [API PUSHER AUTH] - Autorisation r√©ussie');
    
    return NextResponse.json(authResponse);

  } catch (error) {
    console.error('üí• [API PUSHER AUTH] - Erreur interne:', error);
    return new NextResponse('Internal Server Error', { status: 500 });
  }
}

