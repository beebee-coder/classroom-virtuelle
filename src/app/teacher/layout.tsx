import { Header } from '@/components/Header';
import Menu from '@/components/Menu';
import { Sidebar, SidebarContent, SidebarInset, SidebarProvider, SidebarTrigger } from '@/components/ui/sidebar';
import { getTasksForProfessorValidation } from '@/lib/actions/teacher.actions';
import { getServerSession } from 'next-auth';
import { authOptions } from '@/lib/auth-options';
import prisma from '@/lib/prisma';
import { redirect } from 'next/navigation';
import { ChatSheet } from '@/components/ChatSheet';
import { Role } from '@prisma/client';
import { AlertCircle } from 'lucide-react';
export const dynamic = 'force-dynamic';

// Interface pour les donn√©es de classe
interface ClassroomData {
  id: string;
  nom: string;
}

// Composant de layout d'erreur
function TeacherLayoutError({ message }: { message: string }) {
  return (
    <div className="min-h-screen flex items-center justify-center bg-background">
      <div className="text-center p-8 max-w-md">
        <AlertCircle className="h-12 w-12 text-destructive mx-auto mb-4" />
        <h1 className="text-2xl font-bold text-destructive mb-2">Erreur</h1>
        <p className="text-muted-foreground mb-4">{message}</p>
        <button
          onClick={() => window.location.reload()}
          className="px-4 py-2 bg-primary text-primary-foreground rounded-md hover:bg-primary/90"
        >
          R√©essayer
        </button>
      </div>
    </div>
  );
}

export default async function TeacherLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  try {
    const session = await getServerSession(authOptions);
    
    if (!session?.user || session.user.role !== 'PROFESSEUR') {
      console.log('üîí [LAYOUT TEACHER] - Redirection: utilisateur non authentifi√© ou non professeur');
      redirect('/login');
    }

    const user = session.user;
    console.log(`üë®‚Äçüè´ [LAYOUT TEACHER] - Chargement du layout pour: ${user.name}`);

    // Initialisation des donn√©es avec gestion d'erreur
    let classrooms: ClassroomData[] = [];
    let validationCount = 0;
    let firstClassroomId: string | null = null;

    try {
      // R√©cup√©ration des classes avec gestion d'erreur
      classrooms = await prisma.classroom.findMany({
        where: { professeurId: user.id },
        select: { 
          id: true, 
          nom: true 
        },
      });

      // R√©cup√©ration des t√¢ches √† valider avec gestion d'erreur
      try {
        const tasksToValidate = await getTasksForProfessorValidation(user.id);
        validationCount = tasksToValidate.length;
      } catch (validationError) {
        console.error('‚ùå [LAYOUT TEACHER] - Erreur lors du chargement des validations:', validationError);
        // On continue avec validationCount = 0
      }

      // D√©termination de la premi√®re classe
      firstClassroomId = classrooms.length > 0 ? classrooms[0].id : null;

      console.log(`‚úÖ [LAYOUT TEACHER] - Donn√©es charg√©es: ${classrooms.length} classes, ${validationCount} validations`);

    } catch (databaseError) {
      console.error('‚ùå [LAYOUT TEACHER] - Erreur de base de donn√©es:', databaseError);
      // On continue avec les valeurs par d√©faut pour permettre au layout de s'afficher
    }

    return (
      <SidebarProvider>
        <div className="flex flex-col min-h-screen">
          <Header user={user}>
            <SidebarTrigger />
            {firstClassroomId && user.role && (
              <ChatSheet 
                classroomId={firstClassroomId} 
                userId={user.id} 
                userRole={user.role as Role} 
              />
            )}
          </Header>
          <div className="flex flex-1">
            <Sidebar>
              <SidebarContent>
                <Menu 
                  user={user} 
                  classrooms={classrooms} 
                  validationCount={validationCount} 
                />
              </SidebarContent>
            </Sidebar>
            <SidebarInset>
              {children}
            </SidebarInset>
          </div>
        </div>
      </SidebarProvider>
    );

  } catch (criticalError) {
    console.error('üí• [LAYOUT TEACHER] - Erreur critique dans le layout:', criticalError);
    
    // En cas d'erreur critique, on affiche une page d'erreur compl√®te
    return (
      <TeacherLayoutError 
        message="Impossible de charger l'interface professeur. Veuillez v√©rifier votre connexion et r√©essayer." 
      />
    );
  }
}