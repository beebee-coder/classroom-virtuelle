'use client';

import dynamic from 'next/dynamic';
import { useCallback, useEffect, useState, memo, useRef } from 'react';
import { useTheme } from 'next-themes';

// Charger dynamiquement le composant Excalidraw SANS rendu c√¥t√© serveur (SSR)
const Excalidraw = dynamic(
  async () => {
    console.log("DYNAMIC IMPORT: Chargement de @excalidraw/excalidraw");
    const mod = await import('@excalidraw/excalidraw');
    return mod.Excalidraw;
  },
  { 
    ssr: false,
    loading: () => (
      <div className="h-full w-full flex items-center justify-center">
        <p className="text-muted-foreground">Chargement du tableau blanc...</p>
      </div>
    )
  }
);

interface WhiteboardProps {
  sessionId: string;
  onWhiteboardChange: (elements: readonly any[], appState: any, files: any) => void;
  initialElements?: readonly any[];
  initialAppState?: any;
  isController: boolean;
}

// ‚ö†Ô∏è CORRECTION MAJEURE : Optimisation compl√®te pour √©viter les boucles infinies
export const Whiteboard = memo(function Whiteboard({
  sessionId,
  onWhiteboardChange,
  initialElements,
  initialAppState,
  isController,
}: WhiteboardProps) {
  const [excalidrawApi, setExcalidrawApi] = useState<any>(null);
  const { resolvedTheme } = useTheme();
  const [currentTheme, setCurrentTheme] = useState<"light" | "dark">("light");
  const [isExcalidrawReady, setIsExcalidrawReady] = useState(false);
  
  // ‚ö†Ô∏è CORRECTION : R√©f√©rences pour √©viter les cycles de mise √† jour
  const lastInitialElements = useRef<readonly any[] | undefined>();
  const lastInitialAppState = useRef<any>();
  const lastChangeRef = useRef<string>('');
  const isUpdatingScene = useRef(false);
  const changeTimeoutRef = useRef<NodeJS.Timeout>();

  // ‚ö†Ô∏è CORRECTION : Chargement des composants Excalidraw une seule fois
  useEffect(() => {
    let isMounted = true;
    
    const loadExcalidrawComponents = async () => {
      try {
        console.log('üé® [Whiteboard] Chargement des composants Excalidraw...');
        const excalidrawModule = await import('@excalidraw/excalidraw');
        
        if (isMounted) {
          setCurrentTheme(resolvedTheme === 'dark' ? excalidrawModule.THEME.DARK : excalidrawModule.THEME.LIGHT);
          setIsExcalidrawReady(true);
          console.log('‚úÖ [Whiteboard] Composants Excalidraw charg√©s');
        }
      } catch (error) {
        console.error('‚ùå [Whiteboard] Erreur lors du chargement des composants:', error);
      }
    };

    loadExcalidrawComponents();

    return () => {
      isMounted = false;
    };
  }, [resolvedTheme]);

  // ‚ö†Ô∏è CORRECTION CRITIQUE : Mise √† jour optimis√©e des donn√©es initiales avec protection anti-boucle
  useEffect(() => {
    if (!excalidrawApi || isUpdatingScene.current || !initialElements) return;

    const elementsChanged = JSON.stringify(initialElements) !== JSON.stringify(lastInitialElements.current);
    const appStateChanged = JSON.stringify(initialAppState) !== JSON.stringify(lastInitialAppState.current);

    if (elementsChanged || appStateChanged) {
      console.log('üñºÔ∏è [Whiteboard] Mise √† jour de la sc√®ne avec nouvelles donn√©es');
      
      isUpdatingScene.current = true;
      
      excalidrawApi.updateScene({ 
        elements: initialElements,
        appState: initialAppState
      });

      lastInitialElements.current = initialElements;
      lastInitialAppState.current = initialAppState;
      
      // ‚ö†Ô∏è CORRECTION : R√©initialiser le flag apr√®s un d√©lai pour √©viter les conflits
      setTimeout(() => {
        isUpdatingScene.current = false;
      }, 100);
    }
  }, [initialElements, initialAppState, excalidrawApi]);

  // ‚ö†Ô∏è CORRECTION CRITIQUE : Gestion des changements avec debounce et protection anti-boucle
  const handleOnChange = useCallback(
    (elements: readonly any[], appState: any, files: any) => {
      // Ignorer si nous sommes en train de mettre √† jour la sc√®ne ou si pas contr√¥leur
      if (isUpdatingScene.current || !isController) {
        if (!isController) {
          console.log('üö´ [Whiteboard] Changement ignor√© (mode vue seule)');
        }
        return;
      }

      // ‚ö†Ô∏è CORRECTION : Debounce pour √©viter les appels trop fr√©quents
      if (changeTimeoutRef.current) {
        clearTimeout(changeTimeoutRef.current);
      }

      changeTimeoutRef.current = setTimeout(() => {
        // V√©rifier si les donn√©es ont vraiment chang√©
        const currentData = { elements, appState };
        const currentJSON = JSON.stringify(currentData);
        
        if (currentJSON === lastChangeRef.current) {
          console.log('üîÑ [Whiteboard] Changement identique, skip');
          return;
        }
        
        lastChangeRef.current = currentJSON;

        console.log('‚úçÔ∏è [Whiteboard] Changement d√©tect√©, appel de onWhiteboardChange');
        
        if (elements && appState) {
          onWhiteboardChange(elements, appState, files);
        }
      }, 50); // ‚ö†Ô∏è CORRECTION : Debounce de 50ms
    },
    [isController, onWhiteboardChange]
  );

  // ‚ö†Ô∏è CORRECTION : Gestion stable de l'API ref
  const handleExcalidrawAPI = useCallback((api: any) => {
    if (api && api !== excalidrawApi) {
      console.log('üéØ [Whiteboard] API Excalidraw initialis√©e');
      setExcalidrawApi(api);
    }
  }, [excalidrawApi]);

  // ‚ö†Ô∏è CORRECTION : Nettoyage des timeouts
  useEffect(() => {
    return () => {
      if (changeTimeoutRef.current) {
        clearTimeout(changeTimeoutRef.current);
      }
    };
  }, []);

  console.log(`üîÑ [Whiteboard] Rendu - Contr√¥leur: ${isController}, Pr√™t: ${isExcalidrawReady}`);

  // √âviter le rendu pendant le chargement
  if (!isExcalidrawReady) {
    return (
      <div className="h-full w-full flex items-center justify-center">
        <div className="text-center">
          <p className="text-muted-foreground">Chargement du tableau blanc...</p>
          <div className="mt-2 w-8 h-8 border-2 border-primary border-t-transparent rounded-full animate-spin mx-auto"></div>
        </div>
      </div>
    );
  }

  return (
    <div className="h-full w-full">
      <Excalidraw
        excalidrawAPI={handleExcalidrawAPI}
        theme={currentTheme}
        initialData={{
          elements: initialElements || [],
          appState: initialAppState || {},
        }}
        onChange={handleOnChange}
        viewModeEnabled={!isController}
        UIOptions={{
          canvasActions: {
            clearCanvas: isController,
            export: false,
            loadScene: isController,
            saveToActiveFile: false,
            toggleTheme: false,
            saveAsImage: true,
          },
          tools: {
            image: false,
          }
        }}
      />
    </div>
  );
});