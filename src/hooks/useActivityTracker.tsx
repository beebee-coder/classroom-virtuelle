'use client';

import { useEffect, useState, useRef, useCallback } from 'react';
import { pusherClient } from '@/lib/pusher/client';
import type { PresenceChannel, Members } from 'pusher-js';

interface PresenceMember {
  id: string;
  info?: any;
}

interface PusherSubscriptionSucceeded {
  members: Members;
  count: number;
}

interface UseActivityTrackerReturn {
  onlineUsers: string[];
  isConnected: boolean;
  error: string | null;
}

/**
 * Hook personnalis√© pour g√©rer l'abonnement de l'utilisateur
 * √† un canal de pr√©sence Pusher pour sa classe.
 * @param userId - L'ID de l'utilisateur actuel.
 * @param classroomId - L'ID de la classe de l'utilisateur.
 * @param enabled - Un bool√©en pour activer ou d√©sactiver le hook.
 */
export const useActivityTracker = (
  userId?: string, 
  classroomId?: string, 
  enabled: boolean = true
): UseActivityTrackerReturn => {
  const [onlineUsers, setOnlineUsers] = useState<string[]>([]);
  const [isConnected, setIsConnected] = useState(false);
  const [error, setError] = useState<string | null>(null);
  
  const channelRef = useRef<PresenceChannel | null>(null);
  const isSubscribedRef = useRef(false);
  const previousParamsRef = useRef<{ userId?: string; classroomId?: string; enabled: boolean }>({ enabled: true });

  console.log('üîç [PRESENCE HOOK DEBUG] - Param√®tres re√ßus:', { userId, classroomId, enabled });

  // Fonction pour nettoyer l'abonnement
  const cleanupSubscription = useCallback((channelName?: string) => {
    if (channelRef.current) {
      try {
        console.log(`üîö [PRESENCE HOOK] - Nettoyage de l'abonnement pour ${channelName || 'canal inconnu'}`);
        channelRef.current.unbind_all();
        channelRef.current = null;
      } catch (err) {
        console.warn('‚ö†Ô∏è [PRESENCE HOOK] - Erreur lors du nettoyage du canal:', err);
      }
    }
    
    if (channelName) {
      try {
        pusherClient.unsubscribe(channelName);
      } catch (err) {
        console.warn('‚ö†Ô∏è [PRESENCE HOOK] - Erreur lors du d√©sabonnement:', err);
      }
    }
    
    isSubscribedRef.current = false;
    setIsConnected(false);
    setOnlineUsers([]);
  }, []);

  useEffect(() => {
    console.log('üîç [PRESENCE HOOK DEBUG] - useEffect ex√©cut√© avec:', { userId, classroomId, enabled });
    
    const currentParams = { userId, classroomId, enabled };
    const previousParams = previousParamsRef.current;
    
    // V√©rifier si les param√®tres ont r√©ellement chang√©
    const paramsChanged = 
      previousParams.userId !== userId ||
      previousParams.classroomId !== classroomId ||
      previousParams.enabled !== enabled;

    previousParamsRef.current = currentParams;

    // Validation des param√®tres
    if (!enabled) {
      console.log('üïµÔ∏è [PRESENCE HOOK] - Hook d√©sactiv√©');
      if (isSubscribedRef.current) {
        cleanupSubscription();
      }
      return;
    }

    if (!userId || !classroomId) {
      console.log('‚ùå [PRESENCE HOOK] - Infos manquantes:', { userId, classroomId });
      setError('Informations utilisateur ou classe manquantes');
      if (isSubscribedRef.current) {
        cleanupSubscription();
      }
      return;
    }

    const channelName = `presence-class-${classroomId}`;

    // √âviter les r√©initialisations inutiles si les param√®tres sont identiques
    if (isSubscribedRef.current && 
        channelRef.current?.name === channelName && 
        !paramsChanged) {
      console.log('‚è≠Ô∏è [PRESENCE HOOK] - Abonnement d√©j√† actif avec les m√™mes param√®tres, ignor√©');
      return;
    }

    // Nettoyer l'ancien abonnement seulement si les param√®tres ont chang√©
    if (paramsChanged && channelRef.current) {
      const previousChannelName = channelRef.current.name;
      if (previousChannelName !== channelName) {
        cleanupSubscription(previousChannelName);
      }
    }

    console.log(`üïµÔ∏è [PRESENCE HOOK] - Tentative d'abonnement au canal: ${channelName} pour l'utilisateur ${userId}`);

    try {
      // V√©rifier si Pusher est disponible
      if (!pusherClient) {
        throw new Error('Client Pusher non disponible');
      }

      // V√©rifier si on est d√©j√† abonn√© √† ce canal
      const existingChannel = pusherClient.channel(channelName);
      if (existingChannel) {
        console.log('‚úÖ [PRESENCE HOOK] - D√©j√† abonn√© au canal, r√©utilisation');
        channelRef.current = existingChannel as PresenceChannel;
        isSubscribedRef.current = true;
        return;
      }

      // S'abonner au canal
      const channel = pusherClient.subscribe(channelName) as PresenceChannel;
      channelRef.current = channel;
      isSubscribedRef.current = true;
      setError(null);

      // G√©rer la souscription r√©ussie
      const handleSubscriptionSucceeded = (data: PusherSubscriptionSucceeded) => {
        console.log(`‚úÖ [PRESENCE HOOK] - Abonnement r√©ussi au canal ${channelName}`);
        setIsConnected(true);
        
        const userList = Object.keys(data.members || {});
        setOnlineUsers(userList);
        console.log(`üìä [PRESENCE HOOK] - Liste initiale des utilisateurs en ligne:`, userList);
      };

      // G√©rer l'ajout de membre
      const handleMemberAdded = (member: PresenceMember) => {
        console.log(`‚ûï [PRESENCE HOOK] - Nouvel utilisateur connect√©:`, member.id);
        setOnlineUsers(prev => {
          // √âviter les doublons
          if (prev.includes(member.id)) {
            return prev;
          }
          return [...prev, member.id];
        });
      };

      // G√©rer la suppression de membre
      const handleMemberRemoved = (member: PresenceMember) => {
        console.log(`‚ûñ [PRESENCE HOOK] - Utilisateur d√©connect√©:`, member.id);
        setOnlineUsers(prev => prev.filter(id => id !== member.id));
      };

      // G√©rer les erreurs de souscription
      const handleSubscriptionError = (status: any) => {
        console.error(`‚ùå [PRESENCE HOOK] - Erreur d'abonnement au canal ${channelName}:`, status);
        setError(`Erreur d'abonnement: ${status?.message || 'Erreur inconnue'}`);
        setIsConnected(false);
        isSubscribedRef.current = false;
      };

      // G√©rer la d√©connexion du canal
      const handleUnsubscribed = () => {
        console.log(`üîå [PRESENCE HOOK] - D√©sabonn√© du canal ${channelName}`);
        setIsConnected(false);
        isSubscribedRef.current = false;
      };

      // Lier les √©v√©nements
      channel.bind('pusher:subscription_succeeded', handleSubscriptionSucceeded);
      channel.bind('pusher:member_added', handleMemberAdded);
      channel.bind('pusher:member_removed', handleMemberRemoved);
      channel.bind('pusher:subscription_error', handleSubscriptionError);
      channel.bind('pusher:unsubscribed', handleUnsubscribed);

      // G√©rer la d√©connexion Pusher globale
      const handleDisconnected = () => {
        console.log('üîå [PRESENCE HOOK] - D√©connect√© de Pusher');
        setIsConnected(false);
        setError('D√©connect√© du service de pr√©sence');
      };

      const handleConnected = () => {
        console.log('üîó [PRESENCE HOOK] - Reconnect√© √† Pusher');
        setIsConnected(true);
        setError(null);
      };

      pusherClient.connection.bind('disconnected', handleDisconnected);
      pusherClient.connection.bind('connected', handleConnected);

      // Nettoyage
      return () => {
        console.log(`üîö [PRESENCE HOOK] - Nettoyage des √©couteurs pour ${channelName}`);
        
        // Retirer les √©couteurs d'√©v√©nements
        if (channel) {
          channel.unbind('pusher:subscription_succeeded', handleSubscriptionSucceeded);
          channel.unbind('pusher:member_added', handleMemberAdded);
          channel.unbind('pusher:member_removed', handleMemberRemoved);
          channel.unbind('pusher:subscription_error', handleSubscriptionError);
          channel.unbind('pusher:unsubscribed', handleUnsubscribed);
        }
        
        pusherClient.connection.unbind('disconnected', handleDisconnected);
        pusherClient.connection.unbind('connected', handleConnected);
      };

    } catch (error) {
      console.error(`‚ùå [PRESENCE HOOK] - Erreur critique lors de la tentative d'abonnement:`, error);
      setError(`Erreur de connexion: ${error instanceof Error ? error.message : 'Erreur inconnue'}`);
      setIsConnected(false);
      isSubscribedRef.current = false;
    }

    // Cleanup final seulement lors du d√©montage complet
    return () => {
      // Ne pas nettoyer imm√©diatement - laisser Pusher g√©rer la reconnexion
      // Le nettoyage sera g√©r√© par le changement de param√®tres
    };
  }, [userId, classroomId, enabled, cleanupSubscription]);

  return { 
    onlineUsers, 
    isConnected, 
    error 
  };
};