'use client';
import { useState, useCallback } from 'react';

// D√©finir le type ici aussi pour plus de s√©curit√©
export interface ChatMessage {
  role: 'user' | 'assistant' | 'system';
  content: string;
}

export function useDeepSeek() {
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  const chat = useCallback(async (messages: ChatMessage[]): Promise<string> => {
    setIsLoading(true);
    setError(null);
    
    try {
      console.log('üó£Ô∏è [HOOK] - Appel de la route API /api/mcp...');
      const response = await fetch('/api/mcp', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({ messages }),
      });

      if (!response.ok) {
        const errorData = await response.json();
        throw new Error(errorData.error || `Erreur HTTP: ${response.status}`);
      }

      const data = await response.json();
      
      console.log('‚úÖ [HOOK] - R√©ponse re√ßue de /api/mcp.');
      return data.response;

    } catch (err) {
      const errorMessage = err instanceof Error ? err.message : 'Une erreur est survenue lors de la communication avec l\'assistant.';
      console.error('‚ùå [HOOK] - Erreur:', errorMessage);
      setError(errorMessage);
      throw err;
    } finally {
      setIsLoading(false);
    }
  }, []);

  const explainConcept = useCallback(async (concept: string, gradeLevel?: string): Promise<string> => {
    return chat([
      {
        role: 'system',
        content: `Tu es un professeur de coll√®ge sp√©cialis√© pour les √©l√®ves de ${gradeLevel || '6√®me'}. Sois clair, p√©dagogique et utilise des exemples concrets.`
      },
      {
        role: 'user',
        content: `Explique-moi le concept suivant de mani√®re simple: ${concept}`
      }
    ]);
  }, [chat]);

  const helpWithHomework = useCallback(async (subject: string, question: string): Promise<string> => {
    return chat([
      {
        role: 'system',
        content: `Tu es un assistant p√©dagogique. Aide l'√©l√®ve √† comprendre sans donner la r√©ponse directement. Pose des questions pour le guider.`
      },
      {
        role: 'user',
        content: `Sujet: ${subject}\nQuestion: ${question}`
      }
    ]);
  }, [chat]);

  return {
    chat,
    explainConcept,
    helpWithHomework,
    isLoading,
    error,
    clearError: () => setError(null)
  };
}
